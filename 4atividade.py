a = int(input("Digite o numero A: "))
b = int(input("Digite o numero B: "))

soma = a + b
media = soma / 2
produto = a * b



if a == b:
    print("Numeros Iguais")
    
maior = max(a,b)
menor = min(a,b)



print(f"Soma: {soma}")
print(f"Media: {media}")
print(f"Produto: {produto}")
print(f"Maior Numero: {maior}")
print(f"Menor Numero: {menor}")
    